def _make_minimal_xlsx_bytes(extra_columns=None, description_value=None):
    """Build an in-memory XLSX with the ANCHOR_COLUMN and a minimal set of
    REQUIRED_COLUMNS so the header-row detection logic can find it.

    Returns a BytesIO ready to pass to parse_single_file_bytes().
    """
    from core.ingest import REQUIRED_COLUMNS, ANCHOR_COLUMN

    wb = openpyxl.Workbook()
    ws = wb.active

    # Row 1: some junk header (simulates CDARS metadata rows before the real header)
    ws.append(["Report generated by CDARS system", None, None])

    # Row 2: the actual CDARS column header row (contains ANCHOR_COLUMN)
    header_row = list(REQUIRED_COLUMNS)
    if extra_columns:
        header_row += extra_columns
    ws.append(header_row)

    # Row 3: one data row
    data_row = ["P0001"] + ["value"] * (len(header_row) - 1)
    if description_value is not None:
        # Put the value in the "Principal Diagnosis Description (HAMDCT)" column
        try:
            idx = header_row.index("Principal Diagnosis Description (HAMDCT)")
            data_row[idx] = description_value
        except ValueError:
            pass
    ws.append(data_row)

    buf = io.BytesIO()
    wb.save(buf)
    buf.seek(0)
    return buf


