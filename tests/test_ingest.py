class TestParseSingleFileBytes:
    def test_parse_single_file_bytes_success(self):
        """A valid XLSX BytesIO returns (DataFrame, None)."""
        from core.ingest import parse_single_file_bytes

        buf = _make_minimal_xlsx_bytes()
        df, err = parse_single_file_bytes(buf, "test_file.xlsx")

        assert err is None, f"Expected no error, got: {err!r}"
        assert isinstance(df, pd.DataFrame), "Expected a DataFrame"
        assert "Reference Key" in df.columns, "DataFrame must contain 'Reference Key'"

    def test_parse_single_file_bytes_bad_file(self):
        """Garbage bytes return (None, non-empty error string)."""
        from core.ingest import parse_single_file_bytes

        garbage = io.BytesIO(b"\x00\x01\x02\x03garbage not an excel file")
        df, err = parse_single_file_bytes(garbage, "garbage.xlsx")

        assert df is None, "Expected None DataFrame for unparseable file"
        assert isinstance(err, str) and len(err) > 0, (
            "Expected a non-empty error string for unparseable file"
        )


# ---------------------------------------------------------------------------
# FNDN-04 (ingest-time): string columns normalised at parse time
# ---------------------------------------------------------------------------

class TestIngestNormalizesStringCols:
    def test_ingest_normalizes_string_cols(self):
        """An en-dash (U+2013) in a description column must be normalized to
        an ASCII hyphen '-' in the returned DataFrame."""
        from core.ingest import parse_single_file_bytes

        # "Self\u2013harm" contains an en-dash; after normalization it should
        # read "Self-harm"
        en_dash_value = "Self\u2013harm"
        buf = _make_minimal_xlsx_bytes(description_value=en_dash_value)
        df, err = parse_single_file_bytes(buf, "test_file.xlsx")

        assert err is None, f"Parse failed unexpectedly: {err!r}"
        assert df is not None

        desc_col = "Principal Diagnosis Description (HAMDCT)"
        assert desc_col in df.columns, f"Column {desc_col!r} missing from result"

        values = df[desc_col].tolist()
        assert any("Self-harm" in str(v) for v in values), (
            f"Expected en-dash normalized to ASCII hyphen in {values!r}"
        )
        assert not any("\u2013" in str(v) for v in values), (
            f"En-dash U+2013 should have been normalized away, got {values!r}"
        )
